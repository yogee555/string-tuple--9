# Tuple
tup1 = (100,"b","true","c","false")

# find length of tuple
print(len(tup1))

# execute tuple
print(tup1)

# execute type of data
print(type(tup1))

# Extract Individual Element
print(tup1[0])
print(tup1[1])
print(tup1[2])
print(tup1[3])

# Reverse
print(tup1[-1])
print(tup1[-2])
print(tup1[-3])
print(tup1[-4])
print(tup1[-5])

# join
print(tup1[0:3])
print(tup1[1:3])
print(tup1[2:3])
